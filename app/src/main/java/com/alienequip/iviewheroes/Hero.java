package com.alienequip.iviewheroes;

class Hero {
    private String name;
    private String from;
    private String photo;

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    String getFrom() {
        return from;
    }

    void setFrom(String from) {
        this.from = from;
    }

    String getPhoto() {
        return photo;
    }

    void setPhoto(String photo) {
        this.photo = photo;
    }
}
